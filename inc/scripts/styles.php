/**
 * Enqueues front-end CSS for color scheme.
 *
 * @see wp_add_inline_style()
 */
function absolutte_custom_css() {
	/*
	Colors
	*/
	$heroColor = esc_html( get_theme_mod( 'absolutte_featured_color', '#6683e2' ) );
	$secondaryColor = esc_html( get_theme_mod( 'absolutte_secondary_color', '#2fc77c' ) );
	$headings_color = esc_html( get_theme_mod( 'absolutte_headings_color', '#222222' ) );
	$text_color = esc_html( get_theme_mod( 'absolutte_text_color', '#777777' ) );
	$link_color = esc_html( get_theme_mod( 'absolutte_link_color', '#6683e2' ) );
	$content_background_color = esc_html( get_theme_mod( 'absolutte_content_background_color', '#FFFFFF' ) );
	$footer_background = esc_html( get_theme_mod( 'absolutte_footer_background', '#FFFFFF' ) );
	$site_gradient = esc_html( get_theme_mod( 'absolutte_site_gradient', '1' ) );
	$site_background_color = esc_html( get_theme_mod( 'absolutte_site_background_color', '#e08461' ) );
	$logo_color = esc_html( get_theme_mod( 'absolutte_logo_color', '#222222' ) );
	$header_bck_color = esc_html( get_theme_mod( 'absolutte_header_bck_color', '#FFFFFF' ) );
	$header_lines_color = esc_html( get_theme_mod( 'absolutte_header_lines_color', '#EEEEEE' ) );
	$quick_view_bck = esc_html( get_theme_mod( 'absolutte_shop_quick_view_bck', '#efefef' ) );

	$colors = array(
		'heroColor'      => $heroColor,
		'secondaryColor' => $secondaryColor,
		'headings_color' => $headings_color,
		'text_color'     => $text_color,
		'link_color'     => $link_color,
		'content_background_color'     => $content_background_color,
		'footer_background'     => $footer_background,
		'site_gradient'     => $site_gradient,
		'site_background_color'     => $site_background_color,
		'logo_color'     => $logo_color,
		'header_bck_color'     => $header_bck_color,
		'header_lines_color'     => $header_lines_color,
		'quick_view_bck'     => $quick_view_bck,

	);

	$custom_css = absolutte_get_custom_css( $colors );

	wp_add_inline_style( 'absolutte_style', $custom_css );



	/*
	Typography
	*/
	$absolutte_typography_font_family = esc_html( get_theme_mod( 'absolutte_typography_font_family', 'Lato' ) );
	$absolutte_typography_font_family_headings = esc_html( get_theme_mod( 'absolutte_typography_font_family_headings', 'Lato' ) );
	$absolutte_typography_subsets = esc_html( get_theme_mod( 'absolutte_typography_subsets', '' ) );
	$absolutte_typography_font_size = esc_html( get_theme_mod( 'absolutte_typography_font_size', '16' ) );

	$typography = array(
		'font-family' 		   => $absolutte_typography_font_family,
		'font-family-headings' => $absolutte_typography_font_family_headings,
		'font-size'     	   => $absolutte_typography_font_size,
	);

	//Add Google Fonts
	$absolutte_font_subset = '';
	if ( is_array( $absolutte_typography_subsets ) ) {
		$absolutte_font_subset = '&subset=';
		foreach ( $absolutte_typography_subsets as $subset ) {
			$absolutte_font_subset .= $subset . ',';
		}
		$absolutte_font_subset = rtrim( $absolutte_font_subset, ',' );
	}

	$absolutte_google_font = '//fonts.googleapis.com/css?family=' . $absolutte_typography_font_family . ':400,700' . $absolutte_font_subset;
	wp_enqueue_style( 'absolutte_google-font', $absolutte_google_font, array(), '1.0', 'all');

	$absolutte_google_font_headings = '//fonts.googleapis.com/css?family=' . $absolutte_typography_font_family_headings . ':100,400,700' . $absolutte_font_subset;
	wp_enqueue_style( 'absolutte_google-font-headings', $absolutte_google_font_headings, array(), '1.0', 'all');

	$custom_css = absolutte_get_custom_typography_css( $typography );

	wp_add_inline_style( 'absolutte_style', $custom_css );
}
add_action( 'wp_enqueue_scripts', 'absolutte_custom_css' );



